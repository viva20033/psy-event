#!/usr/bin/env node
/**
 * Generates supabase/migrations/002_schedule_2026.sql from schedule-2026-data.mjs
 */
import { writeFileSync } from 'fs';
import { dirname, join } from 'path';
import { fileURLToPath } from 'url';
import { dateForDayIndex, scheduleByDayIndex, dayDates } from './schedule-2026-data.mjs';

const __dirname = dirname(fileURLToPath(import.meta.url));
const TZ = '+03:00';

function esc(s) {
  return s.replace(/'/g, "''");
}

function ts(date, time) {
  const [h, m] = time.split(':');
  return `${date}T${h.padStart(2, '0')}:${m.padStart(2, '0')}:00${TZ}`;
}

const lines = [];
lines.push(`-- Schedule import: Gestalt intensive 19 Jun – 1 Jul 2026`);
lines.push(`-- Generated by: node scripts/generate-schedule-sql.mjs`);
lines.push(`-- Source: scanned 2024 schedule (dates shifted −1 day)`);
lines.push('');
lines.push(`-- Update calendar dates on event days`);
for (let i = 0; i <= 12; i++) {
  const d = dayDates[i];
  const rest = i === 4 || i === 8 ? 'true' : 'false';
  lines.push(
    `UPDATE event_days SET event_date = '${d}', is_rest_day = ${rest} WHERE day_index = ${i};`,
  );
}
lines.push(`UPDATE event_days SET label = 'Отъезд' WHERE day_index = 12;`);
lines.push('');
lines.push(`-- Replace existing schedule (safe re-run after backup)`);
lines.push(`DELETE FROM event_audience WHERE event_id IN (SELECT id FROM schedule_events);`);
lines.push(`DELETE FROM schedule_events;`);
lines.push('');

let sortOrder = 0;
for (let dayIndex = 0; dayIndex <= 12; dayIndex++) {
  const slots = scheduleByDayIndex[dayIndex];
  if (!slots?.length) continue;
  const date = dateForDayIndex(dayIndex);
  lines.push(`-- day_index ${dayIndex} (${date})`);
  for (const slot of slots) {
    sortOrder += 1;
    const desc = slot.description ? `'${esc(slot.description)}'` : 'NULL';
    lines.push(`WITH ins AS (`);
    lines.push(`  INSERT INTO schedule_events (event_day_id, title, description, starts_at, ends_at, sort_order)`);
    lines.push(
      `  SELECT ed.id, '${esc(slot.title)}', ${desc}, '${ts(date, slot.start)}'::timestamptz, '${ts(date, slot.end)}'::timestamptz, ${sortOrder}`,
    );
    lines.push(`  FROM event_days ed WHERE ed.day_index = ${dayIndex}`);
    lines.push(`  RETURNING id`);
    lines.push(`)`);
    lines.push(`INSERT INTO event_audience (event_id, for_everyone)`);
    lines.push(`SELECT id, true FROM ins;`);
    lines.push('');
  }
}

const outPath = join(__dirname, '..', 'supabase', 'migrations', '002_schedule_2026.sql');
writeFileSync(outPath, lines.join('\n'), 'utf8');
console.log('Wrote', outPath);
console.log('Events:', sortOrder);
